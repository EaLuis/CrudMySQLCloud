    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
     
        
        Connection con = null;
         try{
             con = getConection();
             ps = con.prepareStatement("INSERT INTO alumno (clave, nombre, domicilio,telefono,correo, fecha_nacimiento, genero) VALUES(?,?,?,?,?,?,?)");
             ps.setString(1, txtClave.getText());
             ps.setString(2, txtNombre.getText());
             ps.setString(3, txtDomicilio.getText());
             ps.setString(4, txtTelefono.getText());
             ps.setString(5, txtEmail.getText());
             ps.setString(6, txtFecha.getText());
             ps.setString(7, cbxGenero.getSelectedItem().toString());
             
             
             int res = ps.executeUpdate();
             if(res>0){
                 JOptionPane.showMessageDialog(null, "Registro guardado");
                 limpiarCajas();
             } else {
                 JOptionPane.showMessageDialog(null, "Error");
                 limpiarCajas();
             }
             
             con.close();
         } catch(Exception e) {
             System.err.println(e);
         }
    }//GEN-LAST:event_btnGuardarActionPerformed

